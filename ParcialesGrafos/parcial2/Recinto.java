package ParcialesGrafos.parcial2;

public class Recinto {
    private String nombre;
    private int tiempo;

    public Recinto(){
    }

    public int getTiempo() {
        return tiempo;
    }

    public void setTiempo(int tiempo) {
        this.tiempo = tiempo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
